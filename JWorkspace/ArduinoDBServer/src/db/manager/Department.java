package db.manager;

import java.util.Date;

public class Department {
	private String id;
	private String compId;
	private String upperDept;
	private String name;
	private String deptMgr;
	private String deptMgrName;
	private String deputyMgr1;
	private String deputyMgr2;
	private String deptOrderBy;
	private String level;
	private String dept_level;
	private String dept_level_desc;
	private Date datetime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDeptMgr() {
		return deptMgr;
	}
	public void setDeptMgr(String deptMgr) {
		this.deptMgr = deptMgr;
	}
	public String getDeputyMgr1() {
		return deputyMgr1;
	}
	public void setDeputyMgr1(String deputyMgr1) {
		this.deputyMgr1 = deputyMgr1;
	}
	public String getDeputyMgr2() {
		return deputyMgr2;
	}
	public void setDeputyMgr2(String deputyMgr2) {
		this.deputyMgr2 = deputyMgr2;
	}
	public String getDeptOrderBy() {
		return deptOrderBy;
	}
	public void setDeptOrderBy(String deptOrderBy) {
		this.deptOrderBy = deptOrderBy;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public Date getDatetime() {
		return datetime;
	}
	public void setDatetime(Date datetime) {
		this.datetime = datetime;
	}
	public String getCompId() {
		return compId;
	}
	public void setCompId(String compId) {
		this.compId = compId;
	}
	public String getUpperDept() {
		return upperDept;
	}
	public void setUpperDept(String upperDept) {
		this.upperDept = upperDept;
	}
	public String getDept_level() {
		return dept_level;
	}
	public void setDept_level(String dept_level) {
		this.dept_level = dept_level;
	}
	public String getDept_level_desc() {
		return dept_level_desc;
	}
	public void setDept_level_desc(String dept_level_desc) {
		this.dept_level_desc = dept_level_desc;
	}
	public String getDeptMgrName() {
		return deptMgrName;
	}
	public void setDeptMgrName(String deptMgrName) {
		this.deptMgrName = deptMgrName;
	}
	@Override
	public String toString() {
		return "Department [id=" + id + ", compId=" + compId + ", upperDept="
				+ upperDept + ", name=" + name + ", deptMgr=" + deptMgr
				+ ", deptMgrName=" + deptMgrName + ", deputyMgr1=" + deputyMgr1
				+ ", deputyMgr2=" + deputyMgr2 + ", deptOrderBy=" + deptOrderBy
				+ ", level=" + level + ", dept_level=" + dept_level
				+ ", dept_level_desc=" + dept_level_desc + ", datetime="
				+ datetime + "]";
	}
	
}
