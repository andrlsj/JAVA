package db.manager;

import java.util.Date;

public class Employee {
	private String id;
	private String name;
	private String deptId;
	private String email;
	private String jobPlace;
	private String jobPlaceDesc;
	private String jobStatus;
	private String sex;
	private String tel;
	private String jobCate;
	private String jobCateDesc;
	private boolean enable;
	private String companyInCharge;
	
	private Date datetime;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDeptId() {
		return deptId;
	}
	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getJobPlace() {
		return jobPlace;
	}
	public void setJobPlace(String jobPlace) {
		this.jobPlace = jobPlace;
	}
	public String getJobPlaceDesc() {
		return jobPlaceDesc;
	}
	public void setJobPlaceDesc(String jobPlaceDesc) {
		this.jobPlaceDesc = jobPlaceDesc;
	}
	public String getJobStatus() {
		return jobStatus;
	}
	public void setJobStatus(String jobStatus) {
		this.jobStatus = jobStatus;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}	
	public Date getDatetime() {
		return datetime;
	}
	public void setDatetime(Date datetime) {
		this.datetime = datetime;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getJobCate() {
		return jobCate;
	}
	public void setJobCate(String jobCate) {
		this.jobCate = jobCate;
	}
	public String getJobCateDesc() {
		return jobCateDesc;
	}
	public void setJobCateDesc(String jobCateDesc) {
		this.jobCateDesc = jobCateDesc;
	}
	public boolean isEnable() {
		return enable;
	}
	public void setEnable(boolean enable) {
		this.enable = enable;
	}
	public String getCompanyInCharge() {
		//return companyInCharge;
		return deptId.substring(0, 3);
	}
	public void setCompanyInCharge(String companyInCharge) {
		this.companyInCharge = companyInCharge;
	}
	@Override
	public String toString() {
		return "Employee [id=" + id + ", name=" + name + ", deptId=" + deptId
				+ ", email=" + email + ", jobPlace=" + jobPlace
				+ ", jobPlaceDesc=" + jobPlaceDesc + ", jobStatus=" + jobStatus
				+ ", sex=" + sex + ", tel=" + tel + ", jobCate=" + jobCate
				+ ", jobCateDesc=" + jobCateDesc + ", enable=" + enable
				+ ", companyInCharge=" + companyInCharge + ", datetime="
				+ datetime + "]";
	}
		
}
